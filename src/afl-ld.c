/* Main entry point */

int main(int argc, char** argv) {

  s32 pid;
  u32 rand_seed;
  int status;
  u8* inst_ratio_str = getenv("AFL_INST_RATIO");

  struct timeval  tv;
  struct timezone tz;

  clang_mode = !!getenv(CLANG_ENV_VAR);

  if (!getenv("AFL_QUIET")) {

    SAYF(cCYA "afl-ld" VERSION cRST
              " by Marc \"vanHauser\" Heuse <mh@mh-sec.de>\n");

  } else

    be_quiet = 1;

  if (argc < 2) {

    SAYF(
        "\n"
        "This is a helper application for afl-fuzz. It is a wrapper around GNU "
        "'ld',\n"
        "executed by the toolchain whenever using afl-gcc or afl-clang. You "
        "probably\n"
        "don't want to run this program directly.\n\n"

        "Rarely, when dealing with extremely complex projects, it may be "
        "advisable to\n"
        "set AFL_INST_RATIO to a value less than 100 in order to reduce the "
        "odds of\n"
        "instrumenting every discovered branch.\n\n");

    exit(1);

  }

  if (getenv("AFL_LD") == NULL) {

    execvp("/bin/ld", argv);  // <= TODO
    FATAL("Oops, failed to execute '%s' - check your PATH", "/bin/ld");

  }

  gettimeofday(&tv, &tz);

  rand_seed = tv.tv_sec ^ tv.tv_usec ^ getpid();

  srandom(rand_seed);

  edit_params(argc, argv);

  if (inst_ratio_str) {

    if (sscanf(inst_ratio_str, "%u", &inst_ratio) != 1 || inst_ratio > 100)
      FATAL("Bad value of AFL_INST_RATIO (must be between 0 and 100)");

  }

  if (getenv(AS_LOOP_ENV_VAR))
    FATAL("Endless loop when calling 'ld' (remove '.' from your PATH)");

  setenv(AS_LOOP_ENV_VAR, "1", 1);

  if (!just_version) add_instrumentation();

  if (!(pid = fork())) {

    execvp(ld_params[0], (char**)ld_params);
    FATAL("Oops, failed to execute '%s' - check your PATH", ld_params[0]);

  }

  if (pid < 0) PFATAL("fork() failed");

  if (waitpid(pid, &status, 0) <= 0) PFATAL("waitpid() failed");

  if (!getenv("AFL_KEEP_ASSEMBLY")) unlink(modified_file);

  exit(WEXITSTATUS(status));

}

